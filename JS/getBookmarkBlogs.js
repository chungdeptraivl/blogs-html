  var currentPage = 1;
  var postsPerPage = 10;
  
  // Hàm hiển thị các bài viết trên trang hiện tại
  function displayPosts() {
    var postContainer = document.getElementById("postContainer");
    postContainer.innerHTML = ""; // Xóa bài viết hiện tại
  
    var startIndex = (currentPage - 1) * postsPerPage;
    var endIndex = startIndex + postsPerPage;
    var displayedPosts = postData.slice(startIndex, endIndex);
  
    displayedPosts.forEach(function (postData) {
      var postBox = createPost(postData);
      postContainer.appendChild(postBox);
    });
  }
  
  // Hàm tạo một bài viết
  function createPost(postData) {
    var postBox = document.createElement("div");
    postBox.classList.add("post-box");
  
    var postBox = document.createElement("div");
    postBox.classList.add("post-box");
  
    var profileHTML = `
  <div class="heading-post">
      <a class="profile" href="/blogOfUser.html">
          <img src="./assets/images/avt.jpeg" alt="" class="profile-img">
          <span class="profile-name">${postData.profileName}</span>
      </a>
      <span style="margin: 0 16px;"> - </span>
      <span class="post-txt">${postData.postDate}</span>
      <span style="margin: 0 16px;"> - </span>
      <span class="post-txt">${postData.postTime}</span>
      <div class="btn-post-action">
          <a href="/edit-post-page.html" class="btn btn-edit">Sua</a>
          <a href="/delete-post-page.html" class="btn btn-delete">Xoa</a>
      </div>
  </div>
  `;
  
    var postTitleHTML = `<a href="blogpage.html" class="post-title">${postData.postTitle}</a>`;
    var postDescriptionHTML = `<p class="post-description">${postData.postDescription}</p>`;
  
    var topicsHTML = `<div class="topic-list">`;
    postData.topics.forEach(function (topic) {
      topicsHTML += `<a href="/topicBlogs.html" class="topic">${topic}</a>`;
    });
    topicsHTML += `</div>`;
  
    var footerHTML = `
  <div class="footer-post">
      ${topicsHTML}
      <div class="footer-detail">
          <div class="icon icon-view">
              <i class='bx bx-show'></i>
              <p>${postData.viewsCount}</p>
          </div>
          <div class="icon icon-bookmark">
              <i class='bx bx-bookmark' ></i>
              <p>${postData.bookmarkCount}</p>
          </div>
      </div>
  </div>
  `;
  
    // Tạo nội dung của bài viết
    var postContent =
      profileHTML + postTitleHTML + postDescriptionHTML + footerHTML;
    postBox.innerHTML = postContent;
  
    return postBox;
  }
  
  // Hàm chuyển đến trang tiếp theo
  function nextPage() {
    var totalPages = Math.ceil(postData.length / postsPerPage);
    if (currentPage < totalPages) {
      currentPage++;
      document.getElementById("currentPage").textContent = currentPage;
      displayPosts();
    }
  }
  
  // Hàm chuyển đến trang trước đó
  function previousPage() {
    if (currentPage > 1) {
      currentPage--;
      document.getElementById("currentPage").textContent = currentPage;
      displayPosts();
    }
  }
  
  // Hiển thị bài viết trên trang đầu tiên khi trang được tải
  displayPosts();
  